    function determinePlay(op: number, outcome: RoundResult): number {
        if(outcome === RoundResult.Draw) {
            return op;
        }
        else if (outcome === RoundResult.Loss) {
            return wrapValue(op - 1);
        }
        else if (outcome === RoundResult.Win) {
            return wrapValue(op + 1);
        }
    }
    
    function wrapValue(value): number {
        if (value < 1) {
            return 3;
        }
        else if (value > 3) {
            return 1;
        }
        else {
            return value;
        }
    }

